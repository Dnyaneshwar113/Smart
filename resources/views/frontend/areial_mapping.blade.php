                        <h3>Advantages</h3>

                        <p>The basic advantages that an aerial photograph has over a ground-based observation are:</p>

                        <ul>

                            <li>Cheaper than conventional surveying.</li>

                            <li>Worldwide coverage is easily available at different scales.</li>

                            <li>Bird’s eye view of large areas, enabling us to see features of the earth surface in their spatial context. </li>

                            <li>It records the surface features at an instance of exposure.</li>

                            <li>The sensitivity of the Digital Camera used in taking aerial photography is relatively more than the sensitivity of naked eyes.</li>

                            <li>Aerial photographs are normally taken with uniform exposure interval that enables obtaining stereo pair of photographs. Such a pair of photographs help in getting a three-dimensional view of the photographed surface.</li>

                            <li>Geo-referencing (in case of 2D)/Triangulation (in case of 3D) of image pairs are created using input data - Images, Control points & Camera file. Bird’s eye view in different Photogrammetry software is obtained using these image pairs. Project specifications are designed, and mapping is carried out and periodical Quality Checks are performed. Model wise data checking for topological and structural errors is performed to ensure quality data as per the project specification. </li>

                        </ul>

                        <br>

                        <h3>Some advantages of an aerial photograph over a map are as follows: </h3>

                        <ul>

                            <li>It provides a current pictorial view of the ground that no map can equal.</li>

                            <li>It is more readily obtained. The photograph may be in the hands of the user within a few hours after it is taken; a map may take months to prepare.</li>

                            <li>It provides a permanent and objective record of the day-to-day changes with the area.</li>

                            <li>It may be made for places that are inaccessible.</li>

                        </ul>

